package cn.clickwise.clickad.feathouse;

public class ArdbConfigure {

	private String host;
	
	private int port;
	
	private int db;

	public String getHost() {
		return host;
	}

	public void setHost(String host) {
		this.host = host;
	}

	public int getPort() {
		return port;
	}

	public void setPort(int port) {
		this.port = port;
	}

	public int getDb() {
		return db;
	}

	public void setDb(int db) {
		this.db = db;
	}
	
}
